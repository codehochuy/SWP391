package DTO;

/**
 *
 * @author DELL
 */
public class Material {

    private String id;
    private String name;
    private String price;
    private String category;
    private String link;

    public Material() {
    }

    public Material(String id, String name, String price, String category, String link) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.category = category;
        this.link = link;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

}